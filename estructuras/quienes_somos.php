<span class="quienes_somos">¿Quiénes Somos?</span>
<p>Somos un grupo interdisciplinario de profesionales, avalados por una amplia trayectoria y capacitación.
  Nos encontramos abocados al abordaje integral de problemáticas conductuales mediante recursos terapéuticos innovadores y programas científicamente aprobados a nivel mundial.
</br>A través de un coordinado trabajo en equipo, desarrollamos programas  personalizados e integrales, según la necesidad de cada paciente y su familia,  con el objetivo de optimizar la eficacia de nuestro tratamiento.
</br>En todos los casos, nos valemos del máximo respeto, confidencialidad y discreción posibles con las personas asistidas.
</br>El abordaje de la cada problemática se realiza a través de actividades individuales, grupales o familiares, según lo que exija el caso.
</br>Además, desde nuestra fundación ofrecemos programas de asesoramiento integral a empresas con problemáticas relacionadas y efectuamos capacitaciones para profesionales interesados en la temática.
</br>Apostamos por una recuperación integral con una gratificación perdurable y desarrollamos para cada paciente un plan personalizado para lograrlo.
</p>
