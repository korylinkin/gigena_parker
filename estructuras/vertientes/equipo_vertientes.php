<div class="contenedor_info_equipo">
  <p >La Comunidad Terapéutica Profesional Las Vertientes cuenta con un experimentado staff de profesionales que, mediante un trabajo interdisciplinario apuntan al cumplimiento de los objetivos propuestos con el residente y su familia.</p>
<ul class="lista_ventajas" style="text-align:left;">
  <li><i class="fa fa-circle" style=""aria-hidden="true"></i> <strong>Médico Psiquiatra:</strong> Evaluación clínica psiquiátrica de los residentes, diagnostico psiquiátrico presuntivo, planificación terapéutica y supervisión de casos.</li>
  <li><i class="fa fa-circle" style=""aria-hidden="true"></i> <strong>Psicólogo:</strong> Diagnostico multidimensional, trabajo terapéutico dentro de los grupos, supervisión constante del material que se va recolectando a lo largo de las actividades cotidianas en la comunidad con el objetivo de lograr mayor planificación terapéutica. Trabajo junto a las familias.</li>
  <li><i class="fa fa-circle" style=""aria-hidden="true"></i> <strong>Operador Terapéutico:</strong> Realizan intervenciones en espacios cotidianos de alto valor terapéutico. El operador interviene y genera cambios desde una interacción de cercanía.</li>

</ul>
